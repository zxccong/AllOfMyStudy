package com.zxc.eshop.eshopinventory.model;

public class User {

    /**
     * 测试用户姓名
     */
    private String name;

    /**
     * 测试用户年龄
     */
    private Integer age;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getAge() {
        return age;
    }

    public void setAge(Integer age) {
        this.age = age;
    }
}
